    private void updateTransactionTable() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atm","root","");      
            String query = "select id from transaction order by id desc limit 0, 1";
            st = con.createStatement();
            rs = st.executeQuery(query);
            int idToInsert = 0;
            if (rs.next()) {
                idToInsert = rs.getInt(1) + 1;
            } else {
                idToInsert = 1;
            }
            
            query = "insert into transaction values(?,?,?,?,?)";
            ps = con.prepareStatement(query);
            ps.setInt(1, idToInsert);
            ps.setInt(2, accNum);
            ps.setString(3, "Withdraw");
            ps.setInt(4, Integer.valueOf(txtAmount.getText()));
            java.util.Date udt = new java.util.Date();
            java.sql.Date sdt = new java.sql.Date(udt.getTime());
            ps.setDate(5, sdt);
            int row = ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Transaction saved");
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
