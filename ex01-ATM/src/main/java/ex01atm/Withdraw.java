    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        if (txtAmount.getText().isEmpty() || txtAmount.getText().equals(0)) {
            JOptionPane.showMessageDialog(this, "Withdraw can't be empty or 0");
            return;
        }
        if (!NumberUtils.isCreatable(txtAmount.getText())) {
            JOptionPane.showMessageDialog(this, "Withdraw should be numeric");
            return;
        }
        int toWithdraw = Integer.valueOf(txtAmount.getText());
        if (toWithdraw <= 0) {
            JOptionPane.showMessageDialog(this, "Withdraw should be positive");
            return;
        }
        getBalance();
        if (toWithdraw > oldBalance) {
            JOptionPane.showMessageDialog(this, "Can't withdraw more than current balance");
            return;
        }
        try {               
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atm","root","");
            String query = "update account set Balance=? where AccountNumber=?";
            ps = con.prepareStatement(query);
            ps.setInt(1, oldBalance - toWithdraw);
            ps.setInt(2, accNum);
            if (ps.executeUpdate() == 1) {
                JOptionPane.showMessageDialog(this, "Balance updated");
                getBalance();
                updateTransactionTable();
                txtAmount.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Missing information");
            }
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(this, e);
        }  
    }//GEN-LAST:event_btnSubmitMouseClicked

